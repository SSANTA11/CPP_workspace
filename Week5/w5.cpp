#include <iostream>
#include <string>
using namespace std;

class Circle {
private:
    int radius;

public:
    Circle() : Circle(1) {}

    Circle(int r) {
        radius = r;
        cout << "반지름 " << radius << "원 생성\n";
    }

    ~Circle() {
        cout << "반지름 " << radius << "원 소멸\n";
    }

    void setRadius(int r) {
        radius = r;
    }

    double getArea() {
        return 3.14 * radius * radius;
    }
    int getRadius() {
        return radius;
    }
};


