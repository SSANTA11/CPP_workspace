// 함수의 인자 전달방식 리뷰(221pg)
void increase(Circle c) {
    int r = c.getRadius();
    c.setRadius(r+1);
}
int main() {
    Circle waffle(30);
    increase(waffle);
    cout << waffle.getRadius() << endl;
}

